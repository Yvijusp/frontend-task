import { IItem } from '~/services/getUserItems';

export const itemHasWrongEmail = (item: IItem) => {
  const emailRegex =
    /^(([^<>()[\]\\.,;:\s@"]+(\.[^<>()[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/;

  if (item.email.toLowerCase().match(emailRegex)) return true;
  return false;
};
